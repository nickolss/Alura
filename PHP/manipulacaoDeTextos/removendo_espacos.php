<?php

$nome = "     Nickolas          ";

//Trim remove espaços, tabs e outras coisas do começo e do final de uma String
$nomeSemEspacos = trim($nome);

//ltrim remove espaços, tabs e outras coisas apenas do começo de uma String
$nomeSemEspacosNoComeco = ltrim($nome);

//rtrim remove espaços, tabs e outras coisas apenas do final de uma String
$nomeSemEspacosNoFim = rtrim($nome);
